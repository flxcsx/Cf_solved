void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    int t = 0;
    sort(a.begin(), a.end());
    for (int i = 0; i < m; i++)
    {
        if (a[i] < 0)
        {
            t += a[i];
        }
    }
    cout << -1 * t;
}

signed main()
{
    ios::sync_with_stdio(0), cin.tie(0), cout.tie(0);

    int Test = 1;
    // cin >> Test;
    for (int T = 1; T <= Test; T++)
    {
        solve();
    }
    return 0;
}